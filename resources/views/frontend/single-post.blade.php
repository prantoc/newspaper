                        <p>‘এখন আমরা অনেকটাই প্রফিটে বিজনেস শুরু করেছি। নতুন নীতিমালা অনুযায়ী গ্রাহকদের টাকা আমাদের হাতে আসার সুযোগ নেই। এছাড়া তদন্ত করলেই সবাই দেখবেন পুরাতন অর্ডার কী পরিমাণ ডেইলি যাচ্ছে। বিজনেস ডেভেলপমেন্টে লস হয়েছে, সেটা বিজনেস করেই প্রফিট করে ফেলব, এর চেয়ে কম সময়ে। কারণ আমাদের ক্রয় মূল্য বাজার মূল্য থেকে অনেক কম। সরকারের সামগ্রিক কার্যক্রম জনগণের স্বার্থ রক্ষার জন্য।’</p>
