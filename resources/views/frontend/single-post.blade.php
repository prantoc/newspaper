                        <p>শুধু ইভ্যালি নয় <a href="#" target="_blank" rel="follow noopener">১০টির মতো ই-কমার্স প্রতিষ্ঠানের বিরুদ্ধে</a> অভিযোগ উঠেছে। ফলে ইভ্যালি ও আলেশা মার্টসহ ১০টি অনলাইন মার্চেন্টে ক্রেডিট, ডেবিট ও প্রি-পেইড কার্ডের লেনদেন স্থগিত করেছে প্রাইম ব্যাংক, মিউচুয়াল ট্রাস্ট ব্যাংক (এম&zwnj;টি&zwnj;বি), ব্র্যাক ব্যাংক, ব্যাংক এশিয়া এবং ঢাকা ব্যাংকসহ বেশ কয়েকটি বাণিজ্যিক ব্যাংক। ইউসিবি ও সিটি ব্যাংকও তাদের গ্রাহকদের এসব অনলাইন মার্চেন্টে লেনদেনের বিষয়ে সতর্ক করেছে।</p>
